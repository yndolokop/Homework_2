# Задача 4
'''Найти произведение ряда чисел от 1 до 10. Полученный результат вывести на экран(можно поискать в интернете алгоритм факториала в python).'''
'''Факториалом числа называют произведение всех натуральных чисел до этого числа включительно. Например: 5!= 1*2*3*4*5 = 120'''

n = int(input()) # ВВодим число 10
f = 1 # переменная f будет хранить сам факториал числа. Присваиваем единицу, т.к. ноль нельзя, чтобы не получить ноль при перемножениии.
while n > 1: # до тех пор пока сохраняется условие, уменьшаем n и низводим его к единице пока не выйдем из цикла нарушив условие n > 1.
    f = f * n # перемножаем.
    n = n - 1
print(f)


