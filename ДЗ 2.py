'''Найти максимальную цифру в числе'''

n = 7952863 # Возьмем произвольное число
digits = [int(x) for x in str(n)] # трансформируем его в список, пробежавшись по всем цифрам числа, переведенных в строку
myMax = digits[0] # в эту переменную внесем 1ый эллемент списка
for i in range(len(digits)): #запускаем цикл перебора эллементов списка
    if digits[i] > myMax: # сравнивая его
        myMax=digits[i] # присваивая ему значение каждого последующего эллемента до тех пор пока не нарушится условие
print ('Max:', myMax)


# Задача 10
'''Найти количество цифр 5 в числе'''

n = 64355915 # Возьмем произвольное число
token = 5 # наш 'маркер'
count = 0 # счетчик
digits = [int(x) for x in str(n)] # переводим число в список
for i in range(len(digits)): # запускаем цикл
    if token == digits[i]: # сравниваем с каждым последующим эллементом списка в цикле
        count += 1 # считаем и выводим на печать.
print('Колличество пятерок в списке: ', count)
